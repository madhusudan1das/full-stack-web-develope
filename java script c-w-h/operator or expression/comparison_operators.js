let m = 7, n = "7";

console.log(m == n);   // true (loose equality)
console.log(m === n);  // false (strict equality)
console.log(m != n);   // false
console.log(m !== n);  // true
console.log(m > 5);    // true
console.log(m < 10);   // true
console.log(m >= 7);   // true
console.log(m <= 6);   // false
